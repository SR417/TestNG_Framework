package Loops;

public class WhileLoops {
	
	public static void main(String[]args) {
		
		//difference between do and do while
		//Do while atleast runs for one time and then checks the condition
		//While first checks the condition and do not execute if condition fails
		int i=10;
		while (i>=10) {
			System.out.println(i);
			i--;
		}
		
	}

}
